//1. Write a ts program to print day of week name using switch case.
var n1 = 7;
switch (n1) {
    case 1: {
        console.log("Monday");
        break;
    }
    case 2: {
        console.log("Tuesday");
        break;
    }
    case 3: {
        console.log("Wednesday");
        break;
    }
    case 4: {
        console.log("Thursday");
        break;
    }
    case 5: {
        console.log("Friday");
        break;
    }
    case 6: {
        console.log("Saturday");
        break;
    }
    case 7: {
        console.log("Sunday");
        break;
    }
    default: {
        console.log("Enter correct number");
    }
}
